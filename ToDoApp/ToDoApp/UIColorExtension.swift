import Foundation
import UIKit

public extension UIColor {
    static var customYellow: UIColor {
        return UIColor(red: 254/255, green: 203/255, blue: 0, alpha: 1)
    }

    static var customBlue: UIColor {
        return UIColor(red: 0/255, green: 125/255, blue: 155/255, alpha: 1)
    }

    static var whiteTwo: UIColor {
        return UIColor(red: 241/255, green: 241/255, blue: 246/255, alpha: 1)
    }
}
